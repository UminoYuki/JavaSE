package com.yuki.base;

public class Dome1 {
    public static void main(String[] args) {
        //八大基本数据类型

        //整数
        int num1 = 10; //最常用
        byte num2 = 20;
        short num3  =30;
        long num4 = 40L; //Long类型要在后面加L

        //小数：浮点数
        float num5 = 50.1F; //float类型要在后面加个F
        double num6 = 3.1415926535897932846263;

        //字符
        char name = 'A';
        /**
        //字符串,String不是关键字，是类
        String name = "王南杰";
         */

        //布尔值
        boolean flag = true;
        boolean flag1 = false;

    }
}
